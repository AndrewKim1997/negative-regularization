SEED = 7
M = 12
N_TRAIN = 48
N_TEST = 600

# Population covariance eigenvalues (restricted representation)
EIGS = np.array([4.0, 3.0, 2.2, 1.5, 1.0, 0.7, 0.45, 0.28, 0.18, 0.11, 0.07, 0.04], dtype=float)
SQRT_SIG = np.diag(np.sqrt(EIGS))

# Weak-spectrum threshold kappa: weakest four directions
KAPPA = 0.20
WEAK_MASK = EIGS <= KAPPA

# -------------------------
# Helper functions
# -------------------------
def make_beta(mode: str) -> np.ndarray:
    if mode == "weak":
        # Most oracle mass on weak eigendirections
        return np.array([0.08, 0.08, 0.06, 0.05, 0.05, 0.04, 0.10, 0.16, 0.28, 0.36, 0.44, 0.52], dtype=float)
    if mode == "balanced":
        return np.array([0.18, 0.16, 0.15, 0.13, 0.12, 0.11, 0.10, 0.10, 0.10, 0.10, 0.10, 0.10], dtype=float)
    if mode == "strong":
        # Most oracle mass on strong eigendirections
        return np.array([0.52, 0.44, 0.36, 0.28, 0.18, 0.12, 0.08, 0.06, 0.05, 0.04, 0.03, 0.02], dtype=float)
    raise ValueError("mode must be one of {'weak', 'balanced', 'strong'}")

def sample_design(n: int, rng: np.random.Generator) -> np.ndarray:
    return rng.normal(size=(n, M)) @ SQRT_SIG

def sample_dataset(beta_true: np.ndarray, noise_std: float, n_train: int = N_TRAIN, n_test: int = N_TEST, seed: int = 0):
    rng = np.random.default_rng(seed)
    X_train = sample_design(n_train, rng)
    X_test  = sample_design(n_test, rng)
    y_train = X_train @ beta_true + rng.normal(scale=noise_std, size=n_train)
    y_test  = X_test  @ beta_true + rng.normal(scale=noise_std, size=n_test)
    return X_train, y_train, X_test, y_test

def sigma_hat(X: np.ndarray) -> np.ndarray:
    return (X.T @ X) / X.shape[0]

def g_hat(X: np.ndarray, y: np.ndarray) -> np.ndarray:
    return (X.T @ y) / X.shape[0]

def empirical_min_eig(X: np.ndarray) -> float:
    return float(np.linalg.eigvalsh(sigma_hat(X)).min())

def fit_direct_lambda(X: np.ndarray, y: np.ndarray, lam: float) -> np.ndarray:
    A = sigma_hat(X) + lam * np.eye(X.shape[1])
    return np.linalg.solve(A, g_hat(X, y))

def fit_baseline_adjusted(X: np.ndarray, y: np.ndarray, tau0: float, lam_adj: float) -> np.ndarray:
    eta = tau0 + lam_adj
    A = sigma_hat(X) + eta * np.eye(X.shape[1])
    return np.linalg.solve(A, g_hat(X, y))

def predict(X: np.ndarray, beta: np.ndarray) -> np.ndarray:
    return X @ beta

def mse(y_true: np.ndarray, y_pred: np.ndarray) -> float:
    return float(np.mean((y_true - y_pred) ** 2))

def empirical_df_direct(X: np.ndarray, lam: float) -> float:
    vals = np.linalg.eigvalsh(sigma_hat(X))
    if np.any(vals + lam <= 0):
        return np.nan
    return float(np.sum(vals / (vals + lam)))

def empirical_df_adjusted(X: np.ndarray, tau0: float, lam_adj: float) -> float:
    eta = tau0 + lam_adj
    vals = np.linalg.eigvalsh(sigma_hat(X))
    if np.any(vals + eta <= 0):
        return np.nan
    return float(np.sum(vals / (vals + eta)))

def sure_adjusted(X: np.ndarray, y: np.ndarray, tau0: float, lam_adj: float, sigma2: float) -> float:
    beta = fit_baseline_adjusted(X, y, tau0, lam_adj)
    y_hat = predict(X, beta)
    df = empirical_df_adjusted(X, tau0, lam_adj)
    return mse(y, y_hat) + (2.0 * sigma2 * df / X.shape[0]) - sigma2

def sign_label(x: float, tol: float = 1e-10) -> str:
    if x < -tol:
        return "negative"
    if x > tol:
        return "positive"
    return "zero"

def weak_stats(beta_true: np.ndarray, lam: float):
    AW = float(np.sum(beta_true[WEAK_MASK] ** 2))
    AS = float(np.sum(beta_true[~WEAK_MASK] ** 2))
    MW = float(np.sum(EIGS[WEAK_MASK] * beta_true[WEAK_MASK] ** 2))
    MS = float(np.sum(EIGS[~WEAK_MASK] * beta_true[~WEAK_MASK] ** 2))
    BW = float(np.sum(EIGS[WEAK_MASK] * (lam ** 2) / ((EIGS[WEAK_MASK] + lam) ** 2) * beta_true[WEAK_MASK] ** 2))
    BS = float(np.sum(EIGS[~WEAK_MASK] * (lam ** 2) / ((EIGS[~WEAK_MASK] + lam) ** 2) * beta_true[~WEAK_MASK] ** 2))
    rhoW = AW / (AW + AS)
    weak_bias_share = BW / (BW + BS)
    return AW, AS, MW, MS, rhoW, BW, BS, weak_bias_share

def oracle_risk_adjustment(beta_true: np.ndarray, tau0: float, lam_adj: float, noise_std: float, n_train: int = N_TRAIN) -> float:
    eta = tau0 + lam_adj
    sigma_n2 = (noise_std ** 2) / n_train
    bias_term = np.sum(EIGS * (eta ** 2) * (beta_true ** 2) / ((EIGS + eta) ** 2))
    var_term  = sigma_n2 * np.sum((EIGS ** 2) / ((EIGS + eta) ** 2))
    return float(bias_term + var_term)

def oracle_derivative_at_zero(beta_true: np.ndarray, tau0: float, noise_std: float, n_train: int = N_TRAIN) -> float:
    sigma_n2 = (noise_std ** 2) / n_train
    vals = 2.0 * (EIGS ** 2) / ((EIGS + tau0) ** 3) * (tau0 * (beta_true ** 2) - sigma_n2)
    return float(np.sum(vals))

def oracle_best_adjustment(beta_true: np.ndarray, tau0: float, noise_std: float, lam_grid: np.ndarray) -> float:
    risks = np.array([oracle_risk_adjustment(beta_true, tau0, lam, noise_std) for lam in lam_grid], dtype=float)
    return float(lam_grid[np.argmin(risks)])

def select_adjustment_by_sure(X_train, y_train, tau0: float, lam_grid: np.ndarray, sigma2_true: float) -> float:
    best_lam, best_crit = None, None
    for lam in lam_grid:
        if tau0 + lam <= 1e-10:
            continue
        crit = sure_adjusted(X_train, y_train, tau0, lam, sigma2_true)
        if (best_crit is None) or (crit < best_crit):
            best_lam, best_crit = float(lam), float(crit)
    return best_lam

def oracle_best_adjustment_empirical(X_train, y_train, X_test, y_test, tau0: float, lam_grid: np.ndarray):
    best_lam, best_mse = None, None
    for lam in lam_grid:
        if tau0 + lam <= 1e-10:
            continue
        beta = fit_baseline_adjusted(X_train, y_train, tau0, lam)
        te_mse = mse(y_test, predict(X_test, beta))
        if (best_mse is None) or (te_mse < best_mse):
            best_lam, best_mse = float(lam), float(te_mse)
    return best_lam, best_mse

