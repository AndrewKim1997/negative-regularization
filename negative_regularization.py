def prepare_full_design(X_df):
    X = X_df.copy()

    for c in X.columns:
        if str(X[c].dtype) == "bool":
            X[c] = X[c].astype(int)

    num_cols = X.select_dtypes(include=[np.number]).columns.tolist()
    cat_cols = [c for c in X.columns if c not in num_cols]

    X_num = X[num_cols].copy() if num_cols else pd.DataFrame(index=X.index)
    for c in X_num.columns:
        med = X_num[c].median()
        if pd.isna(med):
            med = 0.0
        X_num[c] = X_num[c].fillna(med)

    X_cat = X[cat_cols].copy() if cat_cols else pd.DataFrame(index=X.index)
    if len(cat_cols) > 0:
        for c in X_cat.columns:
            mode = X_cat[c].mode(dropna=True)
            fill = str(mode.iloc[0]) if len(mode) > 0 else "missing"
            X_cat[c] = X_cat[c].fillna(fill).astype(str)
        X_cat = pd.get_dummies(X_cat, drop_first=False)

    X_full = pd.concat(
        [X_num.reset_index(drop=True), X_cat.reset_index(drop=True)],
        axis=1
    )

    scaler = StandardScaler()
    X_scaled = scaler.fit_transform(X_full)
    return X_scaled, X_full.shape[1]

def _select_mixed_indices(total_dim, target_dim, top_share=0.60):
    n_top = int(np.ceil(top_share * target_dim))
    n_top = min(max(1, n_top), target_dim)
    n_bottom = target_dim - n_top

    idx = list(range(n_top))
    if n_bottom > 0:
        idx += list(range(total_dim - n_bottom, total_dim))

    idx = list(dict.fromkeys(idx))
    if len(idx) < target_dim:
        for j in range(total_dim):
            if j not in idx:
                idx.append(j)
            if len(idx) == target_dim:
                break

    return np.array(sorted(idx), dtype=int)

def build_fixed_representation(X_scaled):
    n, p = X_scaled.shape
    pca_cap = min(SOURCE_PCA_CAP, p, max(2, n - 2))
    pca = PCA(n_components=pca_cap, svd_solver="full", random_state=SEED)
    Z_cap = pca.fit_transform(X_scaled)
    eigs_cap = pca.explained_variance_.astype(float)

    target_dim = min(FINAL_REPR_DIM, pca_cap)
    if pca_cap >= MIN_FINAL_DIM:
        target_dim = max(MIN_FINAL_DIM, target_dim)

    idx = _select_mixed_indices(pca_cap, target_dim, top_share=TOP_SHARE)
    Z = Z_cap[:, idx]
    eigs = eigs_cap[idx]

    return Z, eigs, len(idx), pca_cap

