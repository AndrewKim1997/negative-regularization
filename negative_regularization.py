def make_beta_pattern(eigs, alignment, signal_std=1.0):
    m = len(eigs)
    base = np.geomspace(1.0, 0.15, m)

    if alignment == "strong":
        raw = base.copy()
    elif alignment == "weak":
        raw = base[::-1].copy()
    elif alignment == "balanced":
        raw = np.ones(m, dtype=float)
    else:
        raise ValueError("alignment must be one of {'weak','balanced','strong'}")

    raw = raw / np.linalg.norm(raw)
    signal_var = float(np.sum(eigs * (raw ** 2)))
    beta = signal_std * raw / np.sqrt(max(signal_var, 1e-12))
    return beta

def weak_mass_ratio(beta):
    m = len(beta)
    k_weak = max(2, int(np.ceil(0.40 * m)))
    weak_idx = np.arange(m - k_weak, m)
    aw = float(np.sum(beta[weak_idx] ** 2))
    at = float(np.sum(beta ** 2))
    return aw / max(at, 1e-12)

# -------------------------
# Baseline-adjusted negative-capable ridge
# -------------------------
def sigma_hat(X):
    return (X.T @ X) / X.shape[0]

def g_hat(X, y):
    return (X.T @ y) / X.shape[0]

def min_eig_empirical(X):
    vals = np.linalg.eigvalsh(sigma_hat(X))
    return float(vals.min())

def fit_adjusted_ridge(X, y, tau0, adj):
    eta = tau0 + adj
    A = sigma_hat(X) + eta * np.eye(X.shape[1])
    b = g_hat(X, y)
    try:
        beta = np.linalg.solve(A, b)
    except np.linalg.LinAlgError:
        beta = np.linalg.pinv(A) @ b
    return beta

def predict(X, beta):
    return X @ beta

def mse(y_true, y_pred):
    return float(np.mean((y_true - y_pred) ** 2))

def rmse(y_true, y_pred):
    return float(np.sqrt(mse(y_true, y_pred)))

def empirical_df_adjusted(X, tau0, adj):
    eta = tau0 + adj
    vals = np.linalg.eigvalsh(sigma_hat(X))
    if eta <= 1e-12 or np.any(vals + eta <= 1e-12):
        return np.nan
    return float(np.sum(vals / (vals + eta)))

def sure_adjusted(X, y, tau0, adj, sigma2):
    eta = tau0 + adj
    if eta <= 1e-12:
        return np.inf
    beta = fit_adjusted_ridge(X, y, tau0, adj)
    y_hat = predict(X, beta)
    df = empirical_df_adjusted(X, tau0, adj)
    if not np.isfinite(df):
        return np.inf
    return mse(y, y_hat) + (2.0 * sigma2 * df / X.shape[0]) - sigma2

def select_adj_by_sure(X, y, tau0, grid, sigma2):
    best_adj, best_score = None, None
    for adj in grid:
        score = sure_adjusted(X, y, tau0, adj, sigma2)
        if np.isfinite(score) and ((best_score is None) or (score < best_score)):
            best_adj, best_score = float(adj), float(score)
    return best_adj

def oracle_best_adj(X_train, y_train, X_test, y_test, tau0, grid):
    best_adj, best_rmse = None, None
    for adj in grid:
        beta = fit_adjusted_ridge(X_train, y_train, tau0, adj)
        te_rmse = rmse(y_test, predict(X_test, beta))
        if (best_rmse is None) or (te_rmse < best_rmse):
            best_adj, best_rmse = float(adj), float(te_rmse)
    return best_adj, best_rmse

def make_adjustment_grids(tau0):
    pos = tau0 * np.r_[0.0, np.logspace(-3, 1.0, POS_GRID_SIZE)]
    neg = -tau0 * np.r_[0.95, 0.90, 0.85, np.linspace(0.75, 0.05, NEG_GRID_SIZE)]
    pos = np.unique(np.asarray(pos, dtype=float))
    neg = np.unique(np.asarray(neg, dtype=float))
    full = np.unique(np.r_[neg, pos])
    return pos, full

def oracle_derivative_zero(eigs, beta, tau0, noise_std, n_train):
    sigma_n2 = (noise_std ** 2) / n_train
    vals = 2.0 * (eigs ** 2) / ((eigs + tau0) ** 3) * (tau0 * (beta ** 2) - sigma_n2)
    return float(np.sum(vals))

