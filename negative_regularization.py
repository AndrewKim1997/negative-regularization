def stable_int_seed(*parts, mod=2_000_000_000):
    s = "||".join(map(str, parts)).encode("utf-8")
    h = hashlib.sha256(s).hexdigest()
    return int(h[:12], 16) % mod

def _coerce_target(y):
    if isinstance(y, pd.DataFrame):
        if y.shape[1] == 1:
            y = y.iloc[:, 0]
        else:
            y = y.iloc[:, 0]
    elif not isinstance(y, pd.Series):
        y = pd.Series(y)
    y = pd.to_numeric(y, errors="coerce")
    return y

def _finalize_xy(X, y, name):
    X = X.copy()
    y = _coerce_target(y)

    mask = y.notna()
    X = X.loc[mask].reset_index(drop=True)
    y = y.loc[mask].reset_index(drop=True).astype(float)

    keep = ~X.isna().all(axis=1)
    X = X.loc[keep].reset_index(drop=True)
    y = y.loc[keep].reset_index(drop=True)

    return {"name": name, "X": X, "y": y}

def _metadata_target_col(ds):
    meta = getattr(ds, "metadata", None)
    if meta is None:
        return None
    if isinstance(meta, dict):
        return meta.get("target_col", None)
    return getattr(meta, "target_col", None)

def _variables_target_candidates(ds):
    vars_df = getattr(ds, "variables", None)
    if vars_df is None or not isinstance(vars_df, pd.DataFrame):
        return []

    cols_lower = {str(c).strip().lower(): c for c in vars_df.columns}
    name_col = cols_lower.get("name", None)
    role_col = cols_lower.get("role", None)
    if name_col is None or role_col is None:
        return []

    out = []
    for _, row in vars_df.iterrows():
        role = str(row[role_col]).strip().lower()
        if role == "target":
            out.append(row[name_col])
    return out

